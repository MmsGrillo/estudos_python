def soma(x, y):			#Cria função definida com nome soma recebendo valor x, e valor y
	return x+y			#Retorna para os valores de x e y, recebidos antes, como x+y

def multiplicação(x,y):	#Cria função definida com nome multiplicação recebendo valor x, e valor y
	return x*y			#Retorna para os valores de x e y, recebidos antes, como x*y

s= soma(2,3)
m= multiplicação(2,3)

print(soma(s,m))